const mongoose = require('mongoose');
const { Schema, model } = mongoose;

const userSchema = Schema({
    username: {
        type: String,
        required: true,
        unique: true
    },
    email: {
        type: String,
        required: true,
        unique: true,
        lowercase: true
    },
    password: {
        type: String,
        required: true
    }
});

module.exports = model('User', userSchema);